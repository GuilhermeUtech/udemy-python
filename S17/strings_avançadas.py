s = "hello world"

print(s.capitalize()) #primeira letra fica em maiúscula
print(s.upper()) #tudo em maiúiscula
print(s.lower()) #tudo em minússcula
print(s.count("o")) #conta quantas letras passadas como argumento tem na minha string
print(s.find("o")) #procura a posição que se encontra o "o"
print(s.center(20,'z'))
